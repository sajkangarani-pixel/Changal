export function getLanguage(preferences = {}) {
  return preferences.language || DEFAULT_LANGUAGE;
}

export function applyDocumentLanguage(language) {
  const isFa = language === "fa";
  document.documentElement.lang = isFa ? "fa" : "en";
  document.documentElement.dir = isFa ? "rtl" : "ltr";
  document.title = isFa ? "چنگال | کشف بازی" : "Changal | Game Discovery";
}

export function localizeGames(games, language) {
  if (language !== "fa") return games;

  return games.map((game) => {
    const fa = GAME_FA[game.id] || {};
    return {
      ...game,
      title: fa.title || game.title,
      shortDescription: fa.shortDescription || game.shortDescription,
      description: fa.description || game.description,
      displayTags: fa.tags || game.tags,
      equipment: game.equipment.map((item) => ({
        ...item,
        name: EQUIPMENT_FA[item.name] || item.name
      }))
    };
  });
}

export function translateDom(root, language) {
  applyDocumentLanguage(language);
  if (language !== "fa") return;

  translateTextNodes(root);
  translateAttributes(root);
}

export function translateText(value, language = "fa") {
  if (language !== "fa") return value;
  const leading = value.match(/^\s*/)?.[0] || "";
  const trailing = value.match(/\s*$/)?.[0] || "";
  const core = value.trim();
  if (!core) return value;
  return `${leading}${translateCore(core)}${trailing}`;
}

function translateTextNodes(root) {
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      const parent = node.parentElement;
      if (!parent || ["SCRIPT", "STYLE"].includes(parent.tagName)) {
        return NodeFilter.FILTER_REJECT;
      }
      return node.nodeValue.trim() ? NodeFilter.FILTER_ACCEPT : NodeFilter.FILTER_REJECT;
    }
  });

  const nodes = [];
  while (walker.nextNode()) nodes.push(walker.currentNode);
  nodes.forEach((node) => {
    node.nodeValue = translateText(node.nodeValue);
  });
}

function translateAttributes(root) {
  const attributes = ["aria-label", "placeholder", "title", "alt"];
  root.querySelectorAll("*").forEach((element) => {
    attributes.forEach((attribute) => {
      if (element.hasAttribute(attribute)) {
        element.setAttribute(attribute, translateCore(element.getAttribute(attribute)));
      }
    });
  });
}

function translateCore(value) {
  if (!value) return value;
  if (UI_FA[value]) return UI_FA[value];

  for (const [pattern, replacement] of PATTERNS_FA) {
    if (pattern.test(value)) return value.replace(pattern, replacement);
  }

  return value;
}
